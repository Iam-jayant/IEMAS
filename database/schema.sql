-- View: Latest reading per meter
CREATE OR REPLACE VIEW latest_meter_readings AS
SELECT DISTINCT ON (meter_id) 
    meter_readings.*,
    meters.name as meter_name,
    meters.location as meter_location,
    CASE 
        WHEN meter_readings.timestamp > NOW() - INTERVAL '5 minutes' THEN 'online'
        ELSE 'offline'
    END as status
FROM meter_readings
JOIN meters ON meter_readings.meter_id = meters.meter_id
ORDER BY meter_id, timestamp DESC;

COMMENT ON VIEW latest_meter_readings IS 'Latest reading for each meter with online/offline status';

-- View: Active alerts
CREATE OR REPLACE VIEW active_alerts AS
SELECT 
    alerts.*,
    meters.name as meter_name,
    meters.location as meter_location
FROM alerts
JOIN meters ON alerts.meter_id = meters.meter_id
WHERE alerts.dismissed = FALSE
ORDER BY alerts.timestamp DESC;

COMMENT ON VIEW active_alerts IS 'All active (non-dismissed) alerts with meter information';
